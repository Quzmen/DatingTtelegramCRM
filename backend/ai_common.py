def parse_json_reply(raw_text: str) -> dict:
    """Достаёт JSON из ответа модели, снисходительно относясь к ```json
    оберткам, которые модели иногда добавляют вопреки просьбе не делать
    этого."""
    import json

    text = raw_text.strip()
    if text.startswith("```"):
        text = text.strip("`")
        if text.startswith("json"):
            text = text[4:].strip()
    return json.loads(text)

def build_transcript(messages: List[dict], contact_name: str, limit: int) -> str:
    tail = messages[-limit:]
    lines = []
    for m in tail:
        who = "Я" if m["out"] else (contact_name or "Собеседник")
        text = (m["text"] or "").strip()
        if not text:
            continue
        date = m["date"].strftime("%d.%m %H:%M") if m["date"] else ""
        lines.append(f"[{date}] {who}: {text}")
    return "\n".join(lines)



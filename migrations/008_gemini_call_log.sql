-- Миграция для Supabase/PostgreSQL: общий (на все поды/воркеры)
-- лимитер запросов к Gemini, см. backend/models.py:GeminiCallLog и
-- backend/ai_gemini.py:_wait_for_quota_slot().
--
-- Таблица новая, поэтому обычно создаётся автоматически через
-- Base.metadata.create_all() при старте приложения — выполнять этот
-- файл вручную не обязательно. На случай ручного применения через
-- SQL-редактор Supabase: идемпотентен, повторный запуск ничего не
-- сломает.
--
-- Причина появления: реальный лимит free-тарифа Gemini оказался общим
-- на весь GEMINI_API_KEY (20 запросов/мин), а не на один процесс —
-- при нескольких репликах сервиса процессный лимитер в памяти не
-- спасал, т.к. каждая реплика не знала о запросах других. Строки в
-- этой таблице — общая метка "вот здесь и сейчас кто-то из подов
-- сходил в Gemini", по которой считается общий скользящий счётчик за
-- последние 60 секунд. Строки старше минуты периодически удаляются
-- самим приложением (см. _wait_for_quota_slot), поэтому таблица не
-- растёт бесконечно и постоянной чистки через cron не требует.

CREATE TABLE IF NOT EXISTS gemini_call_log (
    id SERIAL PRIMARY KEY,
    created_at TIMESTAMP NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS ix_gemini_call_log_created_at ON gemini_call_log (created_at);
